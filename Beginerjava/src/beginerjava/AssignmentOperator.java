package beginerjava;

public class AssignmentOperator {

	public static void main(String[] args) {
	int x = 3;
	int y = 2;
	
	
	x+=y;
	System.out.println(x);
	
	x-=y;
	System.out.println(x);
	
	x*=y;
	System.out.println(x);
	
	x/=y;
	System.out.println(x);
	
	x%=y;
	System.out.println(x);
	
	
	}

}
